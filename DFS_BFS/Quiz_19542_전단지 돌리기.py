import sys
from collections import defaultdict
sys.setrecursionlimit(10**9)

N, S, D = map(int, input().split())
graph = defaultdict(list)
for _ in range(N-1):
    x, y = map(int, input().split())
    graph[x].append(y)
    graph[y].append(x)
    
subTreeDepth = [0]*(N+1)
visited = [False] * (N+1)
def SetSubTreeDepth(now):
    global subTreeDepth
    depth = 1
    visited[now] = True
    for nxt in graph[now]:
        if not visited[nxt]:
            depth = max(SetSubTreeDepth(nxt), depth)
    
    subTreeDepth[now] = depth    
    return depth +1

SetSubTreeDepth(S)

visited = [False] * (N+1)
result = 0
def DFS(now):
    global result
    visited[now] = True
    
    for nxt in graph[now]:
        if not visited[nxt] and subTreeDepth[nxt] > D:
            result += 1
            DFS(nxt)

DFS(S)
print(result*2)