n = int(input())
degree = [0] * (n+1)
edges = []
for _ in range(n-1):
    u, v = map(int,input().split())
    degree[u] += 1
    degree[v] += 1
    edges.append([u,v])

D = 0
G = 0
detected = [False] * (n+1)
for edge in edges:
    u, v = edge
    if not detected[u] and degree[u]>=3:
        cnt = degree[u]
        G += ((cnt*(cnt-1)*(cnt-2))//6)
        detected[u] = True
    if not detected[v] and degree[v] >=3:
        cnt = degree[v]
        G += ((cnt*(cnt-1)*(cnt-2))//6)
        detected[v] = True
    
    D += ((degree[u]-1) * (degree[v]-1))
    
if D > G*3:
    print('D')
elif D < G*3:
    print("G")
elif D == G*3:
    print("DUDUDUNGA")




